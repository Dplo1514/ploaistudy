a =['','','','','','','']
a[0] = 5 + 8 # 연산의 + == 수학의 +
a[1] = 90 - 10 # 연산의 - == 수학의 -
a[2] = 4 * 7 # 연산의 * == 수학의 *
a[3] = 7/2 #연산의 / == 부동소수점까지 표현하는 나누기
a[4] = 7//2 #연산의 // == 부동소수점을 표현하지 않는 나누기
a[5] = 7 % 3 #연산의 % == 나누고난 후의 나머지를 구한다
a[6] = 3**4 #연산의 ** == 앞의 수를 뒤의 수 만큼 제곱하라 (3의 4제곱)
print(a)